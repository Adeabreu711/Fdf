int	ft_mod(int nb1, int nb2)
{
	return (nb1 % nb2);
}

int	ft_mult(int nb1, int nb2)
{
	return (nb1 * nb2);
}

int	ft_div(int nb1, int nb2)
{
	return (nb1 / nb2);
}

int	ft_sum(int nb1, int nb2)
{
	return (nb1 + nb2);
}

int	ft_sub(int nb1, int nb2)
{
	return (nb1 - nb2);
}
